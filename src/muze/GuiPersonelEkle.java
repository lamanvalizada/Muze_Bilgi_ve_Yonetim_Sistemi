    public boolean isNumeric(String s) {  
        return s != null && s.matches("[-+]?\\d*\\.?\\d+");  
    }
    public void sifirla()
    {
        tcTextField1.setText("");
        isimTextField1.setText("");
        soyIsimTextField2.setText("");
        maasTextField.setText("");
        sifreTextField.setText("");
        maasTextField.setText("");
    }
