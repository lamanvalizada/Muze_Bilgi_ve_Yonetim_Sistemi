    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        isimT=isimTextField1.getText();
        soyisimT=soyIsimTextField2.getText();
        tcT=tcTextField1.getText();
        sifreT=sifreTextField.getText();
        maasT=maasTextField.getText();
        if(isimT.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Isim boş bırakılamaz.");
            sifirla();
        }
        else if(soyisimT.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Soyisim boş bırakılamaz.");
            sifirla();
        }
        else if(tcT.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Tc numarası boş bırakılamaz.");
            sifirla();
        }
        else if(!isNumeric(tcT))
        {
            JOptionPane.showMessageDialog(null, "TC numarası sadece rakamlardan oluşa bilir.");
            sifirla();
        }
        else if(tcT.length()!=11)
        {
           JOptionPane.showMessageDialog(null, "TC numarası 11 rakamdan oluşmalıdır."); 
           sifirla();
        }
        else if(!isNumeric(maasT))
        {
           JOptionPane.showMessageDialog(null, "Maaş sadece rakamlardan oluşa bilir.");         
           sifirla();
        }
        else if(Integer.valueOf(maasT)<=0)
        {
            JOptionPane.showMessageDialog(null, "Maaş değeri sıfırdan düşük olamaz.");
            sifirla();
        }
        else
        {
            try {
                if(ConnectionDB.isciEkle(isimT, soyisimT, sifreT, maasT, tcT))
                {
                    JOptionPane.showMessageDialog(null, "Personel eklendi.");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Eklenemedi.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(GuiPersonelEkle.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
      
    }//GEN-LAST:event_ekleActionPerformed

